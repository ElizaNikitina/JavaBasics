package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//8 primitive datatypes
		// to store whole number -->  byte, short, int, long
		
		byte variableOne=127;
		short variableTwo=32767;
		int variableThree=1000;
		long variable_four=1_000_000;
		
		//to store decimal numbers --> float, double
		
		float variable$Five = 12.9999f;
		double anyName = 132.8998456547387;
		
		//to store single character --> char
		
		char singleValue='F';
		char anotherVariable='%';
		
		//to store boolean values --> boolean
		
		boolean hungry = true;
		boolean thirsty = false;
		
		System.out.println(variableOne);
		
	}

}
