package com.syntax.class07;

public class HomeWork_01 {

	public static void main(String[] args) {
		int x = 1;
		while (x <= 100) {
			System.out.print(x + " ");
			x++;
		}
		System.out.println("");
      
		for (int y = 1; y <= 100; y++) {
			System.out.print(y + " ");
		}
		System.out.println("");
		
		int z=1;
		do {
			System.out.print(z + " ");
			z++;
		}while(z<=100);

	}

}
