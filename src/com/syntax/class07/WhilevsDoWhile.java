package com.syntax.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		
		int x = 11;
		while(x<=5) {
			System.out.println("Hello");
			x++;
		}
		
		System.out.println("--------Using Do While Loop-------");
		
		int y =11; 
		do {
			System.out.println("Hello");
			y++;
		}while(y<=5);

	}

}
